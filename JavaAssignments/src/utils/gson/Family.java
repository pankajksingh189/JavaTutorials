package utils.gson;

public class Family {
private int age;
private String role;

public Family(int age, String role) {
	this.age = age;
	this.role = role;
}

public int getAge() {
	return age;
}
public void setAge(int age) {
	this.age = age;
}
public String getRole() {
	return role;
}
public void setRole(String role) {
	this.role = role;
}


}
